import {assertEquals} from "../../testing/asserts.mjs";
import {mul32, mul64} from "./util.mjs";
Deno.test("[hash/fnv/util] mul32", () => {
  assertEquals(mul32(4294967295, 4294967295), 1);
  assertEquals(mul32(305419896, 3735928559), 1445054984);
  assertEquals(mul32(4129748016, 4285819377), 714314032);
  assertEquals(mul32(1413452818, 2320148559), 972980622);
  assertEquals(mul32(2397139153, 800831980), 1778780588);
  assertEquals(mul32(3929750036, 2744630538), 3724347592);
  assertEquals(mul32(2482525035, 2812165124), 3430441388);
  assertEquals(mul32(4185762712, 3981599297), 591970712);
  assertEquals(mul32(894435729, 3573621831), 2952385335);
  assertEquals(mul32(1193139783, 2862912780), 1128382036);
  assertEquals(mul32(3593705229, 2847681485), 3023774825);
  assertEquals(mul32(3322452172, 3219643588), 368593968);
});
